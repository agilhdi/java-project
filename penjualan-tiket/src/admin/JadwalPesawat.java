    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        DftTblModel_barang = new DefaultTableModel();
        DftTblModel_barang.addColumn("ID PSW");
        DftTblModel_barang.addColumn("NAMA");
        DftTblModel_barang.addColumn("KELAS");
        DftTblModel_barang.addColumn("ASAL");
        DftTblModel_barang.addColumn("TUJUAN");
        DftTblModel_barang.addColumn("BERANGKAT");
        DftTblModel_barang.addColumn("SAMPAI");
        DftTblModel_barang.addColumn("HARGA");
        tableJadwalPesawat.setModel(DftTblModel_barang);
        Connection conn = getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            if(cbb_kategori.getSelectedItem().equals("Kelas")){
                SQL = "select * from jadwal_pesawat where kelas_pesawat like '%"+ inputCari.getText() +"%'";
                System.out.println(SQL);
            }else if(cbb_kategori.getSelectedItem().equals("Asal")){
                SQL = "select * from jadwal_pesawat where asal_pesawat like '%"+ inputCari.getText() +"%'";
                System.out.println(SQL);
            }else if(cbb_kategori.getSelectedItem().equals("ID PSW")){
                SQL = "select * from jadwal_pesawat where id_pesawat like '%"+ inputCari.getText() +"%'";
                System.out.println(SQL);
            }else if(cbb_kategori.getSelectedItem().equals("Nama")){
                SQL = "select * from jadwal_pesawatpesawat where maskapai like '%"+ inputCari.getText() +"%'";
                System.out.println(SQL);
            }else {
                 SQL = "select * from jadwal_pesawat where tujuan like '%"+ inputCari.getText() +"%'";
                 System.out.println(SQL);
            }
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_barang.addRow(new Object[]{
                    res.getString("id_pesawat"),
                    res.getString("maskapai"),
                    res.getString("kelas_pesawat"),
                    res.getString("asal_pesawat"),
                    res.getString("tujuan_pesawat"),
                    res.getString("berangkat"),
                    res.getString("sampai"),
                    res.getString("harga_tiket")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_btnCariActionPerformed

